//    (e.g., -1.5 is larger than -3.0).
//
// Example 1:
//
// Computer: Please enter three numbers:
// User: 4 9 2.5
// Computer: The largest number is 9
//
// Example 2:
//
// Computer: Please enter three numbers:
// User: -5.5 3.14 3.14159
